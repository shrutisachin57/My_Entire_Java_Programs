package BasicKnowledge;

public class MultipleIfBlocks {

	public static void main(String[] args) {
		int a=100;
		int b=200;
		
	if (a<b)
	{
		System.out.println("a<b");
	}
	if (a>b)
	{
		System.out.println("a>b");
	}
	if (a<=b)
	{
		System.out.println("a<=b");
	}
	if (a>=b)
	{
		System.out.println("a>=b");
	}
	if (a==b)
	{
		System.out.println("a==b");
	}
	if (a!=b)
	{
		System.out.println("a!=b");
	}

	}

}
