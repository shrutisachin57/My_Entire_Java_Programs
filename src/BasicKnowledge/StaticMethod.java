package BasicKnowledge;

public class StaticMethod {
	
	static void add()
	{
		int a=20;
		int b=35;
		int sum=a+b;
		System.out.println(sum);
	}
	
	static void sub()
	{
		int a=200;
		int b=35;
		int sub=a-b;
		System.out.println(sub);
	}

	public static void main(String[] args) {
		add();
		sub();

		
	

	}

}
